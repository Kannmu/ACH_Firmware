GPIO_TypeDef *map_pin_name_to_gpio_port(char *pin_name) 
{
    if (pin_name == NULL) return NULL;

    switch(pin_name[1]) 
    { 
        case 'A': return GPIOA;
        case 'B': return GPIOB;
        case 'C': return GPIOC;
        case 'D': return GPIOD;
        case 'E': return GPIOE;
        case 'F': return GPIOF;
        default: return NULL;
    }
}

uint16_t map_pin_name_to_pin_number(char *pin_name) 
{
    // 检查空指针
    if (pin_name == NULL) return 0;

    char *end;
    // strtol函数从字符串的开始位置尝试转换数字，end将指向转换后剩余的部分
    // 由于我们关心的是字符串末尾的数字，我们先找到字符串的末尾
    long pin_number = strtol(pin_name + strlen(pin_name) - 1, &end, 10);

    // 如果end指向的不是字符串的开头，说明末尾确实有数字
    while (end > pin_name && !isdigit(*(end - 1))) {
        end--; // 移动end指针到前一个字符，直到找到数字的开头
    }
    if (end != pin_name) {
        pin_number = strtol(end - 1, NULL, 10); // 重新转换得到完整的数字
    }

    // 基于转换得到的数字返回对应的GPIO_PIN值
    // 这里需要根据实际情况映射到正确的宏定义
    switch(pin_number) {
        case 0: return GPIO_PIN_0;
        case 1: return GPIO_PIN_1;
        case 2: return GPIO_PIN_2;
        case 3: return GPIO_PIN_3;
        case 4: return GPIO_PIN_4;
        case 5: return GPIO_PIN_5;
        case 6: return GPIO_PIN_6;
        case 7: return GPIO_PIN_7;
        case 8: return GPIO_PIN_8;
        case 9: return GPIO_PIN_9;
        case 10: return GPIO_PIN_10;
        case 11: return GPIO_PIN_11;
        case 12: return GPIO_PIN_12;
        case 13: return GPIO_PIN_13;
        case 14: return GPIO_PIN_14;
        case 15: return GPIO_PIN_15;
        default: return GPIO_PIN_0;
    }
}

void HAL_Delay_us(uint32_t nus)
{
    uint32_t ticks;
    uint32_t told,tnow,tcnt=0;
    uint32_t reload=SysTick->LOAD;
    ticks=nus*200; 
    told=SysTick->VAL; 
    while(1)
    {
        tnow=SysTick->VAL;
        if(tnow!=told)
        {
            if(tnow<told)tcnt+=told-tnow;
            else tcnt+=reload-tnow+told;
            told=tnow;
            if(tcnt>=ticks)break; 
        }
    };
}

